// TrailPoint ƒë·∫°i di·ªán cho m·ªôt ƒëi·ªÉm trong v·ªát s√°ng
type TrailPoint struct {
	X, Y  float64
	Alpha float64
	Age   int
}

// Particle ƒë·∫°i di·ªán cho m·ªôt h·∫°t ph√°o hoa
type Particle struct {
	X, Y           float64 // V·ªã tr√≠ hi·ªán t·∫°i
	VX, VY         float64 // V·∫≠n t·ªëc theo tr·ª•c X v√† Y
	StartX, StartY float64 // V·ªã tr√≠ ban ƒë·∫ßu
	Life           int     // Tu·ªïi th·ªç (frames)
	MaxLife        int     // Tu·ªïi th·ªç t·ªëi ƒëa
	Size           float64 // K√≠ch th∆∞·ªõc
	Color          string  // M√†u s·∫Øc (emoji)
	Gravity        float64 // ·∫¢nh h∆∞·ªüng c·ªßa tr·ªçng l·ª±c
	Fade           bool    // C√≥ fade out kh√¥ng
	Alpha          float64 // ƒê·ªô trong su·ªët (0.0-1.0)
	Trail          []TrailPoint // V·ªát s√°ng ph√≠a sau
	Bounce         bool    // C√≥ n·∫£y khi ch·∫°m ƒë·∫•t kh√¥ng
	WindResistance float64 // S·ª©c c·∫£n gi√≥
	Sparkle        bool    // C√≥ l·∫•p l√°nh kh√¥ng
}

// FireworksSystem qu·∫£n l√Ω to√†n b·ªô h·ªá th·ªëng ph√°o hoa
type FireworksSystem struct {
	particles  []Particle
	explosions []Explosion
	canvas     *fyne.Container
	labels     map[int]*widget.Label
	width      float64
	height     float64
	isRunning  bool
	ticker     *time.Ticker
	frameCount int
	maxFrames  int
}

// Explosion ƒë·∫°i di·ªán cho m·ªôt v·ª• n·ªï ph√°o hoa
type Explosion struct {
	X, Y          float64
	ParticleCount int
	Colors        []string
	Speed         float64
	Life          int
	Type          ExplosionType
}

// ExplosionType ƒë·ªãnh nghƒ©a c√°c lo·∫°i n·ªï kh√°c nhau
type ExplosionType int

const (
	Burst    ExplosionType = iota // N·ªï t√°n ra
	Fountain                      // Phun nh∆∞ ƒë√†i phun n∆∞·ªõc
	Spiral                        // Xo·∫Øn ·ªëc
	Heart                         // H√¨nh tr√°i tim
	Star                          // H√¨nh ng√¥i sao
)

// NewFireworksSystem t·∫°o h·ªá th·ªëng ph√°o hoa m·ªõi
func NewFireworksSystem(width, height float64) *FireworksSystem {
	return &FireworksSystem{
		particles:  make([]Particle, 0),
		explosions: make([]Explosion, 0),
		canvas:     container.NewWithoutLayout(),
		labels:     make(map[int]*widget.Label),
		width:      width,
		height:     height,
		maxFrames:  80, // 8 seconds at 10 FPS - Nhanh g·∫•p ƒë√¥i
	}
}

// AddExplosion th√™m m·ªôt v·ª• n·ªï m·ªõi
func (fs *FireworksSystem) AddExplosion(x, y float64, explosionType ExplosionType) {
	colors := []string{"üéÜ", "üéá", "‚ú®", "üåü", "üí´", "‚≠ê", "üí•", "üî•", "üå†", "üéä"}

	explosion := Explosion{
		X:             x,
		Y:             y,
		ParticleCount: 30 + rand.Intn(40), // 30-70 particles - Nhi·ªÅu h∆°n
		Colors:        colors,
		Speed:         4.0 + rand.Float64()*4.0, // 4-8 speed - Nhanh g·∫•p ƒë√¥i
		Life:          20 + rand.Intn(15),       // 20-35 frames - Ng·∫Øn h∆°n ƒë·ªÉ nhanh h∆°n
		Type:          explosionType,
	}

	fs.explosions = append(fs.explosions, explosion)
	fs.createParticlesFromExplosion(explosion)
}

// createParticlesFromExplosion t·∫°o particles t·ª´ v·ª• n·ªï
func (fs *FireworksSystem) createParticlesFromExplosion(explosion Explosion) {
	for i := 0; i < explosion.ParticleCount; i++ {
		var vx, vy float64

		switch explosion.Type {
		case Burst:
			// N·ªï t√°n ra theo m·ªçi h∆∞·ªõng
			angle := rand.Float64() * 2 * math.Pi
			speed := explosion.Speed * (0.5 + rand.Float64()*0.5)
			vx = math.Cos(angle) * speed
			vy = math.Sin(angle) * speed

		case Fountain:
			// Phun l√™n nh∆∞ ƒë√†i phun n∆∞·ªõc
			angle := -math.Pi/2 + (rand.Float64()-0.5)*math.Pi/3 // -60¬∞ to -120¬∞
			speed := explosion.Speed * (0.7 + rand.Float64()*0.6)
			vx = math.Cos(angle) * speed
			vy = math.Sin(angle) * speed

		case Spiral:
			// Xo·∫Øn ·ªëc
			spiralAngle := float64(i) * 0.5
			radius := explosion.Speed * 0.8
			vx = math.Cos(spiralAngle) * radius
			vy = math.Sin(spiralAngle) * radius

		case Heart:
			// H√¨nh tr√°i tim ho√†n h·∫£o v·ªõi ph∆∞∆°ng tr√¨nh Cardioid
			t := float64(i) / float64(explosion.ParticleCount) * 2 * math.Pi
			// Ph∆∞∆°ng tr√¨nh tr√°i tim c·∫£i ti·∫øn
			scale := explosion.Speed * 0.15
			heartX := scale * 16 * math.Pow(math.Sin(t), 3)
			heartY := -scale * (13*math.Cos(t) - 5*math.Cos(2*t) - 2*math.Cos(3*t) - math.Cos(4*t))
			
			// Th√™m animation theo th·ªùi gian ƒë·ªÉ tr√°i tim "ƒë·∫≠p"
			pulse := 1.0 + 0.3*math.Sin(float64(i)*0.5)
			vx = heartX * pulse
			vy = heartY * pulse

		case Star:
			// H√¨nh ng√¥i sao 5 c√°nh ho√†n h·∫£o
			starPoints := 5
			outerRadius := explosion.Speed
			innerRadius := explosion.Speed * 0.4
			
			// T·∫°o 2 layers: outer points v√† inner points
			pointIndex := i % (starPoints * 2)
			angle := float64(pointIndex) * (math.Pi / float64(starPoints))
			
			if pointIndex%2 == 0 {
				// Outer points (ƒëi·ªÉm nh·ªçn)
				vx = math.Cos(angle) * outerRadius
				vy = math.Sin(angle) * outerRadius
			} else {
				// Inner points (ƒëi·ªÉm l√µm)
				vx = math.Cos(angle) * innerRadius
				vy = math.Sin(angle) * innerRadius
			}
		}

		particle := Particle{
			X:              explosion.X,
			Y:              explosion.Y,
			StartX:         explosion.X,
			StartY:         explosion.Y,
			VX:             vx,
			VY:             vy,
			Life:           0,
			MaxLife:        explosion.Life + rand.Intn(30), // Th√™m random cho ƒëa d·∫°ng (30-80 frames)
			Size:           0.8 + rand.Float64()*1.5,       // Size t·ª´ 0.8 ƒë·∫øn 2.3
			Color:          explosion.Colors[rand.Intn(len(explosion.Colors))],
			Gravity:        0.15 + rand.Float64()*0.25, // Gravity t·ª´ 0.15 ƒë·∫øn 0.4 - Nhanh h∆°n
			Fade:           true,
			Alpha:          1.0,                              // B·∫Øt ƒë·∫ßu v·ªõi ƒë·ªô trong su·ªët full
			Trail:          make([]TrailPoint, 0, 3),         // Trail gi·∫£m xu·ªëng 3 ƒëi·ªÉm cho t·ªëc ƒë·ªô
			Bounce:         rand.Float64() < 0.4,             // 40% chance bounce - Nhi·ªÅu action h∆°n
			WindResistance: 0.95 + rand.Float64()*0.03,      // Air resistance 0.95-0.98 - √çt c·∫£n h∆°n
			Sparkle:        rand.Float64() < 0.2,             // 20% chance sparkle
		}

		fs.particles = append(fs.particles, particle)
	}
}

// Update c·∫≠p nh·∫≠t tr·∫°ng th√°i c·ªßa t·∫•t c·∫£ particles
func (fs *FireworksSystem) Update() {
	// C·∫≠p nh·∫≠t particles
	activeParticles := make([]Particle, 0)

	for _, particle := range fs.particles {
		// L∆∞u v·ªã tr√≠ c≈© cho trail effect
		if len(particle.Trail) < cap(particle.Trail) {
			particle.Trail = append(particle.Trail, TrailPoint{
				X: particle.X, Y: particle.Y, Alpha: particle.Alpha, Age: 0,
			})
		} else if len(particle.Trail) > 0 {
			// Shift trail v√† th√™m ƒëi·ªÉm m·ªõi
			copy(particle.Trail[:len(particle.Trail)-1], particle.Trail[1:])
			particle.Trail[len(particle.Trail)-1] = TrailPoint{
				X: particle.X, Y: particle.Y, Alpha: particle.Alpha, Age: 0,
			}
		}

		// C·∫≠p nh·∫≠t age c·ªßa trail points
		for j := range particle.Trail {
			particle.Trail[j].Age++
			particle.Trail[j].Alpha *= 0.8 // Fade trail
		}

		// Advanced Physics
		// 1. √Åp d·ª•ng tr·ªçng l·ª±c
		particle.VY += particle.Gravity

		// 2. Air resistance (s·ª©c c·∫£n kh√¥ng kh√≠)
		particle.VX *= particle.WindResistance
		particle.VY *= particle.WindResistance

		// 3. Hi·ªáu ·ª©ng gi√≥ m·∫°nh h∆°n
		windForce := 0.03 * math.Sin(float64(fs.frameCount)*0.2 + particle.X*0.02)
		particle.VX += windForce

		// 4. Turbulence m·∫°nh cho dynamic movement
		particle.VX += (rand.Float64() - 0.5) * 0.04  // TƒÉng t·ª´ 0.015 l√™n 0.04
		particle.VY += (rand.Float64() - 0.5) * 0.025 // TƒÉng t·ª´ 0.01 l√™n 0.025

		// 5. C·∫≠p nh·∫≠t v·ªã tr√≠
		particle.X += particle.VX
		particle.Y += particle.VY
		particle.Life++

		// 6. Bounce effect m·∫°nh h∆°n khi ch·∫°m ƒë·∫•t
		if particle.Bounce && particle.Y >= fs.height-20 && particle.VY > 0 {
			particle.VY *= -0.8  // Bounce m·∫°nh h∆°n - √≠t energy loss
			particle.VX *= 0.9   // √çt friction h∆°n
			particle.Y = fs.height - 20
			// Th√™m random burst khi bounce
			particle.VX += (rand.Float64() - 0.5) * 0.5
		}

		// 7. Fade effect n√¢ng cao
		if particle.Fade {
			lifeRatio := float64(particle.Life) / float64(particle.MaxLife)
			if lifeRatio > 0.7 {
				// Fade out trong 30% cu·ªëi ƒë·ªùi
				fadeProgress := (lifeRatio - 0.7) / 0.3
				particle.Alpha = 1.0 - fadeProgress
			} else {
				// Fade in trong 10% ƒë·∫ßu ƒë·ªùi
				if lifeRatio < 0.1 {
					particle.Alpha = lifeRatio / 0.1
				}
			}
		}

		// Ki·ªÉm tra boundary
		if particle.X < 0 || particle.X > fs.width ||
			particle.Y > fs.height || particle.Life > particle.MaxLife {
			// Particle ch·∫øt, kh√¥ng th√™m v√†o activeParticles
			continue
		}

		activeParticles = append(activeParticles, particle)

		// C·∫≠p nh·∫≠t particle trong slice
		fs.particles[len(activeParticles)-1] = particle
	}

	fs.particles = activeParticles
	fs.frameCount++
}

// Render v·∫Ω c√°c particles l√™n canvas v·ªõi effects n√¢ng cao
func (fs *FireworksSystem) Render() *fyne.Container {
	// Clear existing labels
	fs.canvas.RemoveAll()
	fs.labels = make(map[int]*widget.Label)

	// Render trails tr∆∞·ªõc
	for _, particle := range fs.particles {
		// V·∫Ω trail (v·ªát s√°ng ph√≠a sau)
		for j, trailPoint := range particle.Trail {
			if trailPoint.Alpha > 0.1 { // Ch·ªâ v·∫Ω khi c√≤n ƒë·ªß s√°ng
				trailLabel := widget.NewLabel("¬∑")
				trailLabel.TextStyle = fyne.TextStyle{Bold: false}
				
				// Size gi·∫£m d·∫ßn theo trail
				trailSize := float32(10 + j*2)
				trailLabel.Resize(fyne.NewSize(trailSize, trailSize))
				trailLabel.Move(fyne.NewPos(float32(trailPoint.X-float64(trailSize/2)), float32(trailPoint.Y-float64(trailSize/2))))
				
				fs.canvas.Add(trailLabel)
			}
		}
	}

	// Render particles ch√≠nh
	for i, particle := range fs.particles {
		// Ch·ªçn emoji d·ª±a tr√™n life cycle v√† effects
		emoji := particle.Color
		lifeRatio := float64(particle.Life) / float64(particle.MaxLife)
		
		if particle.Sparkle && rand.Float64() < 0.3 {
			// Sparkle effect
			sparkleEmojis := []string{"‚ú®", "üí´", "üåü", "‚≠ê"}
			emoji = sparkleEmojis[rand.Intn(len(sparkleEmojis))]
		} else if lifeRatio > 0.8 {
			// Giai ƒëo·∫°n cu·ªëi - fade particles
			if particle.Alpha < 0.5 {
				fadeEmojis := []string{"¬∑", "Àô", "¬∞"}
				emoji = fadeEmojis[rand.Intn(len(fadeEmojis))]
			}
		} else if lifeRatio > 0.6 {
			// Giai ƒëo·∫°n gi·ªØa - transition effects  
			if rand.Float64() < 0.4 {
				transitionEmojis := []string{"‚ú®", "üí´", "‚≠ê"}
				emoji = transitionEmojis[rand.Intn(len(transitionEmojis))]
			}
		}

		// T·∫°o label v·ªõi size d·ª±a tr√™n particle size v√† alpha
		label := widget.NewLabel(emoji)
		label.TextStyle = fyne.TextStyle{Bold: particle.Alpha > 0.7}
		
		// Size thay ƒë·ªïi theo alpha v√† particle size
		displaySize := float32(particle.Size * 15 * particle.Alpha)
		if displaySize < 8 {
			displaySize = 8
		}
		label.Resize(fyne.NewSize(displaySize, displaySize))
		label.Move(fyne.NewPos(float32(particle.X-float64(displaySize/2)), float32(particle.Y-float64(displaySize/2))))

		fs.canvas.Add(label)
		fs.labels[i] = label
	}

	return fs.canvas
}

// Start b·∫Øt ƒë·∫ßu animation
func (fs *FireworksSystem) Start() {
	if fs.isRunning {
		return
	}

	fs.isRunning = true
	fs.frameCount = 0
	fs.ticker = time.NewTicker(100 * time.Millisecond) // 10 FPS - TƒÉng g·∫•p ƒë√¥i t·ªëc ƒë·ªô

	// RAPID FIRE explosions sequence - T·ªëc ƒë·ªô c·ª±c nhanh!
	go func() {
		// Wave 1: Lightning fast opening burst
		fs.AddExplosion(fs.width*0.5, fs.height*0.3, Burst)
		
		time.Sleep(150 * time.Millisecond) // Gi·∫£m t·ª´ 300ms xu·ªëng 150ms
		
		// Wave 2: Rapid symmetric bursts
		fs.AddExplosion(fs.width*0.3, fs.height*0.25, Burst)
		fs.AddExplosion(fs.width*0.7, fs.height*0.25, Burst)
		
		time.Sleep(250 * time.Millisecond) // Gi·∫£m t·ª´ 600ms xu·ªëng 250ms
		
		// Wave 3: Quick romantic heart
		fs.AddExplosion(fs.width*0.5, fs.height*0.4, Heart)
		
		time.Sleep(300 * time.Millisecond) // Gi·∫£m t·ª´ 800ms xu·ªëng 300ms
		
		// Wave 4: Rapid star constellation - ƒë·ªìng lo·∫°t
		fs.AddExplosion(fs.width*0.2, fs.height*0.2, Star)
		time.Sleep(50 * time.Millisecond)  // Rapid fire
		fs.AddExplosion(fs.width*0.8, fs.height*0.2, Star)
		time.Sleep(50 * time.Millisecond)  // Rapid fire
		fs.AddExplosion(fs.width*0.5, fs.height*0.15, Star)
		
		time.Sleep(200 * time.Millisecond) // Gi·∫£m t·ª´ 500ms xu·ªëng 200ms
		
		// Wave 5: Instant fountain duo
		fs.AddExplosion(fs.width*0.25, fs.height*0.7, Fountain)
		fs.AddExplosion(fs.width*0.75, fs.height*0.7, Fountain)
		
		time.Sleep(150 * time.Millisecond) // Gi·∫£m t·ª´ 400ms xu·ªëng 150ms
		
		// Wave 6: Rapid spiral dance
		fs.AddExplosion(fs.width*0.4, fs.height*0.5, Spiral)
		time.Sleep(100 * time.Millisecond) // Rapid fire
		fs.AddExplosion(fs.width*0.6, fs.height*0.5, Spiral)
		
		time.Sleep(250 * time.Millisecond) // Gi·∫£m t·ª´ 700ms xu·ªëng 250ms
		
		// MASSIVE Finale: Triple burst explosion
		fs.AddExplosion(fs.width*0.5, fs.height*0.4, Burst)
		time.Sleep(80 * time.Millisecond)  // Super rapid
		fs.AddExplosion(fs.width*0.45, fs.height*0.35, Burst)
		time.Sleep(80 * time.Millisecond)  // Super rapid  
		fs.AddExplosion(fs.width*0.55, fs.height*0.45, Burst)
		
		time.Sleep(100 * time.Millisecond) // Gi·∫£m t·ª´ 200ms xu·ªëng 100ms
		
		// RAPID Final sparkles - machine gun style
		for i := 0; i < 5; i++ { // TƒÉng t·ª´ 3 l√™n 5 explosions
			x := fs.width * (0.2 + rand.Float64()*0.6) // R·ªông h∆°n: 20%-80%
			y := fs.height * (0.1 + rand.Float64()*0.4) // Cao h∆°n: 10%-50%
			fs.AddExplosion(x, y, Burst)
			time.Sleep(60 * time.Millisecond) // Gi·∫£m t·ª´ 150ms xu·ªëng 60ms - machine gun!
		}
	}()

	go func() {
		defer fs.ticker.Stop()
		defer func() { fs.isRunning = false }()

		for range fs.ticker.C {
			if fs.frameCount >= fs.maxFrames && len(fs.particles) == 0 {
				return
			}

			fyne.DoAndWait(func() {
				fs.Update()
				fs.Render()
			})
		}
	}()
}

// Stop d·ª´ng animation
func (fs *FireworksSystem) Stop() {
	fs.isRunning = false
	if fs.ticker != nil {
		fs.ticker.Stop()
	}
}

// IsRunning ki·ªÉm tra xem animation c√≥ ƒëang ch·∫°y kh√¥ng
func (fs *FireworksSystem) IsRunning() bool {
	return fs.isRunning
}

